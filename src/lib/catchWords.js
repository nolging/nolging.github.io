// 비교용 정규화: 공백 제거 + 소문자
export function normWord(s) {
  return String(s || '').replace(/\s+/g, '').toLowerCase()
}
// 길이 힌트 (글자 수)
export function wordLen(s) {
  return String(s || '').replace(/\s+/g, '').length
}
